+ (NSString *)showWeekByDate:(NSString *)dateStr{
    
    NSDateFormatter *dateFormatter = [[NSDateFormatter alloc] init];
    
    [dateFormatter setDateFormat:@"yyyy-MM-dd"];
    NSDate *date = [dateFormatter dateFromString:dateStr];
    NSTimeInterval  timeInterval = [date timeIntervalSinceNow];
    NSLog(@"%.f",-timeInterval/(24*60*60));
    NSString *currentDateStr = [dateFormatter stringFromDate:[NSDate date]];
    if ([dateStr isEqualToString:currentDateStr]) {
        return  @"今天";
    }
    NSCalendar * calendar = [NSCalendar autoupdatingCurrentCalendar];
    NSDateComponents *componets = [calendar components:NSWeekdayCalendarUnit fromDate:date];
    int weekday =(int) [componets weekday];
    NSDateComponents *curComponets = [calendar components:NSWeekdayCalendarUnit fromDate:[NSDate date]];
    int curWeekday =(int) [curComponets weekday];
    CGFloat intervalDay =(int) -timeInterval/(24*60*60);
    if(weekday == 1){
        weekday = 7;
    }else{
        weekday -= 1;
    }
    
    if (curWeekday == 1) {
        curWeekday = 7;
    }else{
        curWeekday -= 1;
    }
    
    if (intervalDay < 7 && curWeekday >= weekday) {
        if (weekday == 1) {
            return @"星期一";
        }else if(weekday == 2){
            return @"星期二";
        }else if(weekday == 3){
            return @"星期三";
        }else if(weekday == 4){
            return @"星期四";
        }else if(weekday == 5){
            return @"星期五";
        }else if(weekday == 6){
            return @"星期六";
        }else if(weekday == 7){
            return @"星期日";
        }else {
            return dateStr;
        }
    }else{
        return dateStr;
    }
}
